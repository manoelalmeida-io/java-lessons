    private void btnComecarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComecarActionPerformed
        if (timer != null && timer.isRunning()) {
            return;
        }
        
        if (recomecar) {
            time = LocalTime.MIN;
            recomecar = false;
        }
        
        ActionListener action = new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent arg0) {
                time = time.plusSeconds(1);
                atualizar(time);
            }
        };
        
        timer = new Timer(1000, action);
        timer.start();
    }//GEN-LAST:event_btnComecarActionPerformed

    private void btnPausarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPausarActionPerformed
        timer.stop();
    }//GEN-LAST:event_btnPausarActionPerformed

    private void btnPararActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPararActionPerformed
        timer.stop();
        recomecar = true;
    }//GEN-LAST:event_btnPararActionPerformed

    private void atualizar(LocalTime time) {
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("mm:ss");
        lblTimer.setText(time.format(formatter));
    }

